from __future__ import annotations

import json
import re
from dataclasses import dataclass
from pathlib import Path
from typing import Mapping


_TEMPLATE_TOKEN_RE = re.compile(r"\{\{\s*(?P<name>[A-Za-z_][A-Za-z0-9_]*)\s*\}\}")
_DEFAULT_TEMPLATE_FILENAME = "default_templates.json"


@dataclass(frozen=True, slots=True)
class ProjectTemplate:
    template_id: str
    label: str
    description: str
    files: dict[str, str]
    source_file: str


@dataclass(frozen=True, slots=True)
class ProjectTemplateLoadResult:
    templates: list[ProjectTemplate]
    warnings: list[str]
    template_dir: Path


