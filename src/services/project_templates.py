def project_template_dir(ide_app_dir: str | Path) -> Path:
    return Path(ide_app_dir).expanduser().resolve() / "templates"


def ensure_bootstrap_project_templates(ide_app_dir: str | Path) -> Path:
    templates_dir = project_template_dir(ide_app_dir)
    templates_dir.mkdir(parents=True, exist_ok=True)
    default_file = templates_dir / _DEFAULT_TEMPLATE_FILENAME
    if default_file.exists():
        return default_file
    payload = _default_project_template_payload()
    default_file.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")
    return default_file


def _coerce_template_entry(raw: object, *, source_file: str) -> tuple[ProjectTemplate | None, str | None]:
    if not isinstance(raw, Mapping):
        return None, "Template entry is not an object."
    template_id = str(raw.get("id") or "").strip()
    label = str(raw.get("label") or "").strip()
    description = str(raw.get("description") or "Project template").strip() or "Project template"
    files_raw = raw.get("files")
    if not template_id:
        return None, "Template is missing a non-empty 'id'."
    if not label:
        return None, f"Template '{template_id}' is missing a non-empty 'label'."
    if not isinstance(files_raw, Mapping):
        return None, f"Template '{template_id}' is missing a 'files' object."
    files: dict[str, str] = {}
    for key_obj, value_obj in files_raw.items():
        key = str(key_obj or "").strip()
        if not key:
            continue
        files[key] = str(value_obj or "")
    if not files:
        return None, f"Template '{template_id}' has no valid file entries."
    return (
        ProjectTemplate(
            template_id=template_id,
            label=label,
            description=description,
            files=files,
            source_file=source_file,
        ),
        None,
    )


def _extract_template_entries(data: object) -> list[object] | None:
    if isinstance(data, list):
        return data
    if isinstance(data, Mapping):
        entries = data.get("templates")
        if isinstance(entries, list):
            return entries
    return None


def _fallback_default_templates() -> list[ProjectTemplate]:
    payload = _default_project_template_payload()
    raw_entries = _extract_template_entries(payload) or []
    out: list[ProjectTemplate] = []
    for raw in raw_entries:
        template, _warning = _coerce_template_entry(raw, source_file=_DEFAULT_TEMPLATE_FILENAME)
        if isinstance(template, ProjectTemplate):
            out.append(template)
    return out


def load_project_templates(ide_app_dir: str | Path) -> ProjectTemplateLoadResult:
    templates_dir = project_template_dir(ide_app_dir)
    bootstrap_file = ensure_bootstrap_project_templates(ide_app_dir)
    warnings: list[str] = []

    templates_by_id: dict[str, ProjectTemplate] = {}
    ordered_ids: list[str] = []

    for path in sorted(templates_dir.glob("*.json"), key=lambda p: p.name.lower()):
        source_name = str(path.name)
        try:
            raw_data = json.loads(path.read_text(encoding="utf-8"))
        except Exception as exc:
            warnings.append(f"{source_name}: could not read template file ({exc}).")
            continue

        entries = _extract_template_entries(raw_data)
        if entries is None:
            warnings.append(f"{source_name}: expected a JSON object with 'templates' list or a top-level list.")
            continue
        for raw_entry in entries:
            template, warning = _coerce_template_entry(raw_entry, source_file=source_name)
            if warning:
                warnings.append(f"{source_name}: {warning}")
                continue
            assert template is not None
            tid = template.template_id
            if tid not in templates_by_id:
                ordered_ids.append(tid)
            else:
                prev = templates_by_id[tid]
                warnings.append(
                    f"{source_name}: overriding template '{tid}' previously loaded from {prev.source_file}."
                )
            templates_by_id[tid] = template

    templates = [templates_by_id[tid] for tid in ordered_ids if tid in templates_by_id]
    if not templates:
        templates = _fallback_default_templates()
        warnings.append(
            "No valid project templates were loaded from templates/*.json; using built-in fallback templates."
        )

    if not bootstrap_file.exists():
        warnings.append("Could not create default template bootstrap file.")

    return ProjectTemplateLoadResult(
        templates=templates,
        warnings=warnings,
        template_dir=templates_dir,
    )


def _render_template_tokens(text: str, variables: Mapping[str, str]) -> str:
    source = str(text or "")
    values = {str(k): str(v) for k, v in dict(variables or {}).items()}

    def _replace(match: re.Match[str]) -> str:
        key = str(match.group("name") or "")
        return values.get(key, match.group(0))

    return _TEMPLATE_TOKEN_RE.sub(_replace, source)


def render_project_template_files(template: ProjectTemplate, variables: Mapping[str, str]) -> dict[str, str]:
    out: dict[str, str] = {}
    for raw_path, raw_content in template.files.items():
        path = _render_template_tokens(str(raw_path or ""), variables).strip()
        if not path:
            continue
        out[path] = _render_template_tokens(str(raw_content or ""), variables)
    return out


